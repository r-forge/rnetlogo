<div class="article">
<h2><span>What is it?</span></h2>

<div class="clr"></div>

<p>The RNetLogo package delivers an interface to use and access <a href="http://ccl.northwestern.edu/netlogo/" target="_blank">NetLogo</a>
from <a href="http://www.r-project.org/" target="_blank">GNU
R</a> using either headless (no Graphical User Interface) or
interactive GUI mode. It provides functions to load models, execute
commands, push values and to get values from
NetLogo reporters. The interface is mostly equivalent to NetLogo's
<a href="http://ccl.northwestern.edu/netlogo/docs/mathematica.html" target="_blank">Mathematica Link</a>.
</p>

</div>

<div class="article">
<h2><span>How to get it?</span></h2>

<div class="clr"></div>

<p>The easiest way is to type <i>install.packages("RNetLogo")</i>
in
your R shell - or for Windows users with the RGui to click on <i>Packages
-&gt; Install
package(s)...</i> and select RNetLogo. This will install the
version hosted on <a href="http://cran.r-project.org/web/packages/RNetLogo/index.html" target="_bank">CRAN</a>. The latest package version
will be available here on <a href="https://r-forge.r-project.org/R/?group_id=1202">R-Forge</a> (can be broken during development of new releases). If you don't want to download it manually you can type <i>install.packages("RNetLogo", repos="http://R-Forge.R-project.org")</i> in your R shell.
</p>

</div>

<div class="article">
<h2><span>Where to find more information?</span></h2>

<div class="clr"></div>

<p>The first choice should always be the manual. All functions of
the RNetLogo package come with a manual page. The complete manual as
pdf is available on <a href="http://cran.r-project.org/web/packages/RNetLogo/index.html" target="_blank">CRAN</a>.
Furthermore, a 30 pages tutorial has been accepted for publication by <a href="http://www.jstatsoft.org/v58/i02/" target="_blank">Journal of Statistical Software</a>.
Moreover, there are code samples to all functions which can be found in
the folder <i>examples/code_samples</i> in the
installation path.
A manuscript of a short overview article was accepted by <a href="http://onlinelibrary.wiley.com/doi/10.1111/j.2041-210X.2011.00180.x/abstract" target="_blank">Methods in Ecology and Evolution</a>. 
</p>

</div>

<div class="article">
<h2><span>What else?</span></h2>

<div class="clr"></div>

<p>If you are looking for the other way around, i.e. embed R into
NetLogo models, don't hestitate to try the <a href="http://r-ext.sourceforge.net/" target="_blank">R-Extension
for NetLogo</a> and/or the <a href="http://rserve-ext.sourceforge.net">RServe-Extension</a>. 
The latter is designed to work if you're using RNetLogo. The first
one won't work in conjunction with RNetLogo.<br />
See also our simulation software tools overview webpage at <a href="http://www.uni-goettingen.de/en/315075.html">http://www.uni-goettingen.de/en/315075.html</a>.
</p>

