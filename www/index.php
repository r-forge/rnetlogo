<div class="article">
<h2><span>What is it?</span></h2>

<div class="clr"></div>

<p>The RNetLogo package delivers an interface to embed <a href="http://ccl.northwestern.edu/netlogo/" target="_blank">NetLogo</a>
into the <a href="http://www.r-project.org/" target="_blank">GNU
R</a> environment with headless (no Graphical User Interface) and
interactive GUI mode. It provides functions to load models, execute
commands, push values and to get values from
NetLogo reporters. The interface is mostly equivalent to NetLogos'
<a href="http://ccl.northwestern.edu/netlogo/docs/mathematica.html" target="_blank">Mathematica Link</a>.
</p>

</div>

<div class="article">
<h2><span>How to get it?</span></h2>

<div class="clr"></div>

<p>The easiest way is to type <i>install.packages("RNetLogo")</i>
in
your R shell - or for Windows users with the RGui to click on <i>Packages
-&gt; Install
package(s)...</i> and select RNetLogo. This will install the
version hosted on <a href="http://cran.r-project.org/web/packages/RNetLogo/index.html" target="_bank">CRAN</a>. The latest package version
will be available here on <a href="https://r-forge.r-project.org/R/?group_id=1202">R-Forge</a>.
</p>

</div>

<div class="article">
<h2><span>Where to find more information?</span></h2>

<div class="clr"></div>

<p>The first choice should always be the manual. All functions of
the RNetLogo package come with a manual page. The complete manual as
pdf is available on <a href="http://cran.r-project.org/web/packages/RNetLogo/index.html" target="_blank">CRAN</a>.
Furthermore, there is a more than 30 pages tutorial, which comes with
the package. Just browse to your installation path of the RNetLogo
package and look into the folder <i>tutorial</i>.
Moreover, there are code samples to all functions which can be found in
the folder <i>examples/code_samples</i> in the
installation path.
A manuscript of a short overview article was submitted to <a href="http://www.methodsinecologyandevolution.org/view/0/index.html" target="_blank">Methods in Ecology and Evolution</a>.
</p>

</div>

<div class="article">
<h2><span>What else?</span></h2>

<div class="clr"></div>

<p>If you are looking for the other way around, i.e. embed R into
NetLogo models, don't hestitate to try the <a href="http://netlogo-r-ext.berlios.de/" target="_blank">R-Extension
for NetLogo</a>.
There are two different version: R-Extension and RServe-Extension. The
latter is designed to work if you're using RNetLogo. The first
one won't work in conjunction with RNetLogo.
</p>

</div>

